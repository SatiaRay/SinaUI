export const toEnglishLetter = (char) => {
  if (!char) return '';
  const map = {
    ا: 'A',
    ب: 'B',
    پ: 'P',
    ت: 'T',
    ث: 'S',
    ج: 'J',
    چ: 'CH',
    ح: 'H',
    خ: 'KH',
    د: 'D',
    ذ: 'Z',
    ر: 'R',
    ز: 'Z',
    ژ: 'ZH',
    س: 'S',
    ش: 'SH',
    ص: 'S',
    ض: 'Z',
    ط: 'T',
    ظ: 'Z',
    ع: 'A',
    غ: 'GH',
    ف: 'F',
    ق: 'Q',
    ک: 'K',
    گ: 'G',
    ل: 'L',
    م: 'M',
    ن: 'N',
    و: 'V',
    ه: 'H',
    ی: 'Y',
    ء: 'A',
  };
  return map[char] || char.toUpperCase();
};
